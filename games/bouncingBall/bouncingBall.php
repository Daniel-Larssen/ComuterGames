function initInput() {
  document.addEventListener("keydown", keyDown);  // Actionlistener for keypress.
  document.addEventListener("keyup", keyUp);      // Actionlistener for keyrelease. 
}

// The keyDown event that happens when a key is pressed.
function keyDown(evt) {
  
  if(evt.keyCode == 37) { evt.preventDefault(); leftKeyDown = true; }
  if(evt.keyCode == 39) { evt.preventDefault(); rightKeyDown = true; }
  if(evt.keyCode == 38) { evt.preventDefault(); if(ballOnFloor) { ballMovementY = -11; }}
}

// The keyUp event that happens when a key is released.
function keyUp(evt) {
  if(evt.keyCode == 37) { leftKeyDown = false; }
  if(evt.keyCode == 39) { rightKeyDown = false; }
  if(evt.keyCode == 38) { if(ballOnFloor) { ballMovementY = -11; }}
}

// The function that moves the ball.
function ballMove() {

    // If the balls is on the floor... Else the ball is in the air. 
    if(ballOnFloor) {
      ballMovementX *= 0.9;  // Ground friction
    } else {
      ballMovementX *= 0.5;  // Air resistance
      ballMovementY += 1.3;

      // Limits the speed of the ball, bigger number is faster.
      if(ballMovementY > 5) {
        ballMovementY = 5;
      }
    }

    // If the left or right-button is pressed
    if(leftKeyDown) {
      ballMovementX = -4;  // Speed to the left
    }
    if(rightKeyDown) {
      ballMovementX = 4;   // Speed to the right
    }

    // Stops the ball from going trough the bottom of the tile.
    if(ballMovementY < 0 && checkIfTile(ballX,ballY-ballRadius) == 1) {
      ballY = (Math.floor( ballY / tileHeight )) * tileHeight + ballRadius;
      ballMovementY = 0.0;
    }
    
    // Stops the ball from going trough the floor.
    if(ballMovementY > 0 && checkIfTile(ballX,ballY+ballRadius) == 1) {
      ballY = (1+Math.floor( ballY / tileHeight )) * tileHeight - ballRadius;
      ballOnFloor = true;
      ballMovementY = 0;
    } else if(checkIfTile(ballX,ballY+ballRadius+2) == 0) {
      ballOnFloor = false;
    }
    
    // Checks here the walls to the left. 
    if(ballMovementX < 0 && checkIfTile(ballX-ballRadius,ballY) == 1) {
      ballX = (Math.floor( ballX / tileWidth )) * tileWidth + ballRadius;
    }

    // Checks here the walls to the right.
    if(ballMovementX > 0 && checkIfTile(ballX+ballRadius,ballY) == 1) {
      ballX = (1+Math.floor( ballX / tileWidth )) * tileWidth - ballRadius;
    }
    
    ballX += ballMovementX; // Adds the horizontal velocity found above. 
    ballY += ballMovementY; // same as above, but for vertical.
  }

/**
 * This checks if there is a tile at the next
 * position of the 
 */
function checkIfTile(nextX, nextY) {
  var currentTileY = nextX / tileWidth;		
  var currentTileX = nextY / tileHeight;

  currentTileY = Math.floor( currentTileY );
  currentTileX = Math.floor( currentTileX );

  if(currentTileY < 0 || currentTileY >= tileHor || currentTileX < 0 || currentTileX >= tileVer) {
    return false;
  }

var tileNumber = (currentTileY + tileHor*currentTileX);      // Calculates the index number of the tile.
return (currentBitMap[tileNumber] == 1);                            // 
}

// Colors the tiles that are placed to act as walls.
function colorRect(topLeftX, topLeftY, tileWidth, tileHeight, fillColor) {
  canvasContext.fillStyle = fillColor;
  canvasContext.fillRect(topLeftX, topLeftY, tileWidth, tileHeight);
}

// Colors the ball that are placed where the player moves it.
function colorBall(centerX, centerY, radius, fillColor) {
  canvasContext.fillStyle = fillColor;
  canvasContext.beginPath();
  canvasContext.arc(centerX, centerY, radius, 0, Math.PI*2, true);
  canvasContext.fill();
}

/**
 * Draws all of the tiles on the canvas, looks like
 * squares. Uses the top lef x and y to place them.
 */
function drawTiles() {

  // For each of the horisontal columns of the bitmap.
  for(var eachTileX=0; eachTileX<tileHor; eachTileX++) {
    // For each of the vertical columns of the bitmap. 
    for(var eachTileY=0; eachTileY<tileVer; eachTileY++) { 

      // If tile is there, returns 1 if it is.
      if( (currentBitMap[eachTileX + tileHor*eachTileY] == 1) ) {
        var brickLeftEdgeX = eachTileX * tileWidth;
        var brickTopEdgeY = eachTileY * tileHeight;

        // Colors the current tile.
        colorRect(brickLeftEdgeX, brickTopEdgeY, tileWidth, tileHeight, 'yellow' );
      } 
    } 
  } 
} 
